#include "../inc/mega.h"

int main(void) {
    /*
     *****************************************************************
     *                    Hardware init in app                       *
     * ***************************************************************
     */
    appBoot();
    /*
     *****************************************************************
     *                    system init                                *
     * ***************************************************************
     */
    setupHwTimer0();
    systemInit();
    /*
     ********************************************************************
     *                    UARTS interface Init                           *
     * ******************************************************************
     */
#if defined USART_MODULE
#if USART_MODULE
#if defined USART0_ENABLED
    usart0Init();
#elif defined USART1_ENABLED
    usart1Init();
#elif defined USART2_ENABLED
    usart2Init();
#elif defined USART3_ENABLED
    usart3Init();
#endif
#endif 
#endif

    /*
     *****************************************************************
     *              Two wire interface Init (I2C) (TWI)              *
     * ***************************************************************
     */
#if defined TWI_MODULE
#if (TWI_MODULE) 
#if (TWI_TYPE == TWI_MASTER_SLAVE) || (TWI_TYPE == TWI_MASTER)
    twi_master_init();
#endif
#endif
#endif

    /*
     *****************************************************************
     *                    EEPROM init                                *
     * ***************************************************************
     */
#if (defined EX_EEPROM_MODULE )
#if EX_EEPROM_MODULE 
    exEepromInit();
#endif
#endif
    /*
     *****************************************************************
     *                    DS1307 init                                *
     * ***************************************************************
     */
#if (defined DS1307_MODULE)
#if DS1307_MODULE 
    ds1307Init();
#endif
#endif




    /*
     *****************************************************************
     *                    KEYPDA init                                *
     * ***************************************************************
     */
#if defined (KEYPAD_MODULE)
#if KEYPAD_MODULE 
    keyInit();
#endif    
#endif
    /*
     *****************************************************************
     *                    LCD init                                *
     * ***************************************************************
     */
#if defined(LCD_MODULE)
#if LCD_MODULE 
    lcdHwInit();
#endif
#endif

    /*
     *****************************************************************
     *                    app init                                *
     * ***************************************************************
     */
    appInit();
    /*
     ***********************************************************************
     *                    system start init                                *
     * *********************************************************************
     */
    startHwTimer0();

    while (1) {
        /*
         *****************************************************************
         *                    EEPROM Driver                                *
         * ***************************************************************
         */
#if defined EX_EEPROM_MODULE
#if EX_EEPROM_MODULE
        exeepromDriver();
#endif
#endif
        /*
         *****************************************************************
         *                    DS1307 Driver                                *
         * ***************************************************************
         */

#if defined DS1307_MODULE
#if DS1307_MODULE
        ds1307Driver();
#endif
#endif




        /*
         *****************************************************************
         *                    LCD Driver                                *
         * ***************************************************************
         */
#if defined (LCD_MODULE)
#if LCD_MODULE
        lcdDriver();
#endif
#endif
        /*
         *****************************************************************
         *                    app main                                *
         * ***************************************************************
         */
        appMain();
    }
    return (0);
}

